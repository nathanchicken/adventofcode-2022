export const YourMap = {
  X: 'Rock',
  Y: 'Paper',
  Z: 'Scissors'
} as const

export type TheirKey = keyof typeof TheirMap
export type Option = 'Rock' | 'Paper' | 'Scissors'
export type YourKey = keyof typeof YourMap

export const ScoreMap = {
  X: 1,
  Rock: 1,
  Y: 2,
  Paper: 2,
  Z: 3,
  Scissors: 3
} as const

export const VictoryMap = {
  win: 6,
  draw: 3,
  lose: 0
} as const

export type VictoryKey = keyof typeof VictoryMap

export const check = (them: TheirKey, you: YourKey): VictoryKey => {
  const theirGo = TheirMap[them]
  const yourGo = YourMap[you]

  if (yourGo === theirGo) return 'draw'

  if (yourGo === 'Rock') {
    if (theirGo === 'Scissors') return 'win'
    if (theirGo === 'Paper') return 'lose'
  }

  if (yourGo === 'Paper') {
    if (theirGo === 'Rock') return 'win'
    if (theirGo === 'Scissors') return 'lose'
  }

  if (yourGo === 'Scissors') {
    if (theirGo === 'Paper') return 'win'
    if (theirGo === 'Rock') return 'lose'
  }

  return 'draw'
}

export const WinStatusMap = {
  X: 'lose',
  Y: 'draw',
  Z: 'win'
} as const

export const beats = {
  Rock: 'Scissors',
  Scissors: 'Paper',
  Paper: 'Rock'
} as const

export const loses = {
  Scissors: 'Rock',
  Paper: 'Scissors',
  Rock: 'Paper'
} as const

export const checkSecret = (them: TheirKey, you: YourKey): Option => {
  const theirGo = TheirMap[them]
  const requirement = WinStatusMap[you]

  if (requirement === 'win') {
    return loses[theirGo]
  }

  if (requirement === 'lose') {
    return beats[theirGo]
  }

  return TheirMap[them]
}

